async function main() {
  const projectId = process.env.FIREBASE_PROJECT_ID;
  const clientEmail = process.env.FIREBASE_CLIENT_EMAIL;
  const privateKey = process.env.FIREBASE_PRIVATE_KEY?.replace(/\\n/g, '\n');

  if (!projectId || !clientEmail || !privateKey) {
    throw new Error('Заполни FIREBASE_* в .env.local перед запуском импорта.');
  }
  if (!getApps().length) {
    initializeApp({ credential: cert({ projectId, clientEmail, privateKey }) });
  }

  const db = getFirestore();

  // 1) Удаляем демо-товары.
  for (const id of DEMO_IDS) {
    await db.collection('products').doc(id).delete();

    console.log(`🗑️  удалён демо: ${id}`);
  }

  // 2) Создаём/обновляем реальные товары.
  for (const { id, ...data } of products) {
    const ref = db.collection('products').doc(id);
    const existed = (await ref.get()).exists;
    await ref.set({ ...data, ...(KASPI[id] ? { kaspiUrl: KASPI[id] } : {}) }, { merge: true });

    console.log(`${existed ? '♻️  обновлён' : '🆕 создан'}: ${id}`);
  }

  console.log(`✅ Товаров в каталоге: ${products.length}`);
}

main().catch((err) => {
  console.error('❌ Ошибка импорта:', err);
  process.exit(1);
});
